package pruebaswitch;

/**
 *
 * @author Alumno
 */
public class PruebaSwitch
{

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) 
    {
        String palabra;
        palabra="uno";
        switch(palabra)
        {
            case "dos":
                System.out.print("H2OLA  ");
            case "tres":
                System.out.print("HOLA1  ");
            case "uno":
                System.out.print("HOLA  ");
               
            default:
                System.out.println("HOsLA");
            
            
            
            
            
            
        }
        
        
        
        
        
        
    }
    
}
